interface IShape {
    draw(): void;
}

function drawShape(shape: IShape): void {
    shape.draw();
}

class Circle1 implements IShape {
    draw =  function(): void {
        console.log(`Draw circle with radius ${this.radius}`);
    }
    constructor(private radius: number) { }
}

let shape: IShape = new Circle1(1);
drawShape(shape);

class Rectangle implements IShape {
    draw = function (): void {
        console.log(`Draw rectangle with a: ${this.a} and b: ${this.b}`);
    }
    constructor(private a: number, private b: number) { }
}

shape = new Rectangle(2,3);
drawShape(shape);

class Triangle implements IShape {
    draw = function (): void {
        console.log(`Draw triangle with a: ${this.a} and b: ${this.b} and c: ${this.c}`);
    }
    constructor(private a: number, private b: number, private c: number) { }
}

shape = new Triangle(4,5,6);
drawShape(shape);




